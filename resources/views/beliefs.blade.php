                    <x-list-item title="Scripture.">
                        The Bible is the fully inspired, infallible and inerrant Word of
                        God, and therefore the only supreme rule for faith and life.
                    </x-list-item>
                    <x-list-item title="God.">
                        There is one God, who decreed all things from eternity, created the
                        entire universe in six days, and governs the whole creation
                        continually.
                    </x-list-item>
                    <x-list-item title="The Trinity.">
                        This one God exists eternally in three Persons: Father, Son, and
                        Holy Spirit.
                    </x-list-item>
                    <x-list-item title="Man.">
                        Man was originally created in the image of God, and his main duty in
                        life is to know, love, glorify and enjoy God.
                    </x-list-item>
                    <x-list-item title="Sin.">
                        All fell with Adam in his first transgression. Everyone is guilty of
                        sin (which is disobeying God's law), and deserves to be punished in
                        hell for ever.
                    </x-list-item>
                    <x-list-item title="Election.">
                        From all eternity God sovereignly elected some of fallen mankind to
                        salvation and eternal life, and entered into a covenant of grace to
                        deliver them out of the estate of sin and misery.
                    </x-list-item>
                    <x-list-item title="Jesus Christ.">
                        God in His mercy sent His only begotten Son to be the Saviour of
                        sinners. By taking human nature into union with His divine Person,
                        Jesus Christ is both God and man. He was born of the virgin Mary,
                        lived a perfectly sinless life, died on the cross, rose bodily from
                        the dead, and ascended into heaven.
                    </x-list-item>
                    <x-list-item title="Atonement.">
                        When Jesus Christ died on the cross, He was punished in the place of
                        His people for their sins, as the only way to satisfy divine
                        justice.
                    </x-list-item>
                    <x-list-item title="Faith.">
                        Those who trust in Christ alone for the forgiveness of their sins,
                        and repent, are accepted by God and will go to heaven. No one can be
                        saved in any other way.
                    </x-list-item>
                    <x-list-item title="The Holy Sprit.">
                        Only the Holy Spirit can bring people to faith and repentance, which
                        He does when He regenerates them into spiritual life.
                    </x-list-item>
                    <x-list-item title="The Second Coming.">
                        Jesus will return bodily at the Day of Judgment, when everyone who
                        died will rise again. Those who trusted Him for salvation will go to
                        heaven with Him for ever, but the rest will be cast into hell to
                        endure conscious torment for ever.
                    </x-list-item>
